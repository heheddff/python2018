import turtle
turtle.screensize(800,800,"green")
turtle.penup()
turtle.goto(0,-200)
turtle.pendown()
turtle.pensize(3)
turtle.pencolor("red")
turtle.speed(3)
turtle.circle(20)
turtle.circle(40)
turtle.circle(80)
turtle.circle(160)
turtle.hideturtle()
turtle.penup()
turtle.goto(-40,80)
turtle.pendown()
turtle.write("同切圆",font=("",18))
turtle.done()
